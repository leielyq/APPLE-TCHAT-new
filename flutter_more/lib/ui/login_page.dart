  void sendMsg(){
    var phone = signupNameController.text;
    if(phone.isEmpty){
      showInSnackBar("请输入手机号");
      return;
    }

    HttpUtil.post('auth/actionCode', data: {
      "mobile": phone,
    }, headers: {}, success: (data) {
      showInSnackBar("验证码发送成功");
    }, error: (errorMsg) {
      showInSnackBar(errorMsg);
    });
  }

  void signUp() {
    var phone = signupNameController.text;
    var invite = signupEmailController.text;
    var code = signupCodeController.text;
    var pass01 = generateMd5(signupPasswordController.text);
    var pass02 = generateMd5(signupConfirmPasswordController.text);
    var psd = "";

    if(phone.isEmpty){
      showInSnackBar("请输入手机号");
      return;
    }
    if(invite.isEmpty){
      showInSnackBar("请输入邀请人");
      return;
    }
    if(code.isEmpty){
      showInSnackBar("请输入手机验证码");
      return;
    }
    if(pass01.isEmpty){
      showInSnackBar("请输入密码");
      return;
    }
    if(pass02.isEmpty){
      showInSnackBar("请再一次输入密码");
      return;
    }
    if(pass01 != pass02){
      showInSnackBar("两次密码输入不一致");
      psd= pass02;
      return;
    }



    HttpUtil.post('auth/register', data: {
      "username": phone,
      "mobile": phone,
      "refereeuser": invite,
      "username": phone,
      "userCode": code,
      "realname": phone,
      "password": pass01,
      "password2": pass02
    }, headers: {}, success: (data) {
      showInSnackBar("注册完成");
      _onSignInButtonPress();
      loginEmailController.text = phone;
      loginPasswordController.text = psd;
    }, error: (errorMsg) {
      showInSnackBar(errorMsg);
    });
  }

